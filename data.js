export function normalizeText(value = "") {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[“”"']/g, "")
    .replace(/[.,!?;:()[\]{}]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

export function buildCulturalIndex() {
  const byKey = new Map();
  const aliasToKey = new Map();

  Object.entries(CULTURAL_MAP).forEach(([key, value]) => {
    const normalizedKey = normalizeText(key);
    byKey.set(normalizedKey, value);

    (value.aliases || []).forEach((alias) => {
      const normalizedAlias = normalizeText(alias);
      if (normalizedAlias) {
        aliasToKey.set(normalizedAlias, normalizedKey);
      }
    });
  });

  return { byKey, aliasToKey };
}
