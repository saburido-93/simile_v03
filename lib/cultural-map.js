export function normalizeText(value) {
  return String(value || "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim()
    .replace(/\s+/g, " ");
}

export function buildCulturalIndex() {
  const byKey = new Map();
  const aliasToKey = new Map();

  for (const [key, value] of Object.entries(CULTURAL_MAP)) {
    const normKey = normalizeText(key);
    byKey.set(normKey, { key: normKey, ...value });

    for (const alias of value.aliases || []) {
      aliasToKey.set(normalizeText(alias), normKey);
    }
  }

  return { byKey, aliasToKey };
}
